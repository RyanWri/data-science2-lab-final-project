    test_predictions_final = model(X_test_tensor).numpy()
    y_test_np = y_test_tensor.numpy()

    # Calculate MAE
    mae = mean_absolute_error(y_test_np, test_predictions_final)

    # Calculate RMSE
    rmse = np.sqrt(mean_squared_error(y_test_np, test_predictions_final))

    # Calculate R-squared
